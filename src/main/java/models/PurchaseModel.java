package models;

//Model for that is for dealing with pruchases that the user makes
public class PurchaseModel {
    private int purchaseId;// id from historical data generated by db
    private int userId; // user id
    private int vehicleId; // id of the vheicle purchased
    private int vehiclePrice; // price of that purchased vehicle

    public PurchaseModel(Object object, int userId, int vehicleId, int vehiclePrice) {
        this.purchaseId = (int) object;
        this.vehicleId = vehicleId;
        this.userId = userId;
        this.vehiclePrice = vehiclePrice;
    }

    // getter and setter functions
    public int getPurchaseId() {
        return purchaseId;
    }

    public void setPurchaseId(int purchaseId) {
        this.purchaseId = purchaseId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getVehicleId() {
        return vehicleId;
    }

    public void setVehicleId(int vehicleId) {
        this.vehicleId = vehicleId;
    }

    public int getVehiclePrice() {
        return vehiclePrice;
    }

    public void setVehiclePrice(int vehiclePrice) {
        this.vehiclePrice = vehiclePrice;
    }

}
